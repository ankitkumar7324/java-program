public class arithmetic
{
    public static void main(String args[])
    {
        int a=12;
        int b=5;
        System.out.println("a+b= "+(a+b));
        System.out.println("a-b= "+(a-b));
        System.out.println("a*b= "+(a*b));
        System.out.println("a/b= "+(a/b));
        // relational operator
        System.out.println((a>b));
        System.out.println((a<b));
        System.out.println((a>=b));
        System.out.println((a<=b));
        System.out.println((a==b));
        System.out.println((a!=b));
        }
}